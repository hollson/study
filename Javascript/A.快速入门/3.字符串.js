var txt = "javascript";
txt.charAt()                  //返回索引位字符。
txt.concat(" very", " good")  //连接字符串。
txt.indexOf("a")              //首次索引位。
txt.lastIndexOf("a")          //末次索引位
txt.match()                   //正则匹配。
txt.replace("java", "type")   //替换
txt.slice(0, 4)               //切片
txt.split("s")                //分割
txt.substr(0, 4)              //取n个
txt.substring()               //取区间
txt.toLowerCase()             //小写
txt.toUpperCase()             //大写。
txt.valueOf()                 //原始值。
txt.bold()                    //"<b>javascript</b>"